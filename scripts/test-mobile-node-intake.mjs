import assert from "node:assert/strict";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { normalizeMobileNodeInput, persistMobileNodeInput } from "./lib/mobile-node-intake.mjs";
const appRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const root = path.join(appRoot, "output", "mobile-node-test");
fs.rmSync(root, { recursive: true, force: true });
const png = Buffer.from([137,80,78,71,13,10,26,10,0,0,0,0]).toString("base64");
assert.equal(normalizeMobileNodeInput({ nodeId: "android-test", mimeType: "image/png", dataBase64: png }).bytes.length, 12);
const receipt = persistMobileNodeInput(root, { nodeId: "android-test", mimeType: "image/png", dataBase64: png, note: "Work-order evidence" }, "QUARANTINED");
assert.equal(receipt.executionAllowed, false);
assert.equal(receipt.pairingStatus, "QUARANTINED");
assert.ok(fs.existsSync(path.join(appRoot, receipt.artifactPath)));
assert.throws(() => normalizeMobileNodeInput({ nodeId: "x", mimeType: "image/png", dataBase64: png }), /node ID/);
assert.throws(() => normalizeMobileNodeInput({ nodeId: "android-test", mimeType: "text/html", dataBase64: png }), /Unsupported/);
assert.throws(() => normalizeMobileNodeInput({ nodeId: "android-test", mimeType: "image/jpeg", dataBase64: png }), /signature/);
fs.rmSync(root, { recursive: true, force: true });
console.log("Mobile node intake checks passed.");
